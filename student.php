  <script>
    const POSITIONS = [
      "President",
      "Vice President",
      "Secretary",
      "Auditor",
      "Treasurer",
      "PIO",
      "PO",
      "Sergeant-at-Arms",
      "Escort",
      "Muse"
    ];

    const CANDIDATES_PER_POSITION = 5;
    const elections = document.getElementById("elections");

    POSITIONS.forEach((position) => {
      const section = document.createElement("div");
      section.className = "section";

      section.innerHTML = `
        <h2>${position}</h2>
        <div class="vote-count">Votes used: <span>0</span> / 1</div>
        <div class="grid"></div>
      `;

      const grid = section.querySelector(".grid");
      const counter = section.querySelector("span");

      for (let i = 1; i <= CANDIDATES_PER_POSITION; i++) {
        const card = document.createElement("div");
        card.className = "card";
        card.innerHTML = `
          <img src="https://i.pravatar.cc/300?img=${Math.floor(
            Math.random() * 70 + 1
          )}">
          <h3>${position} Candidate ${i}</h3>
          <div class="check">âœ“</div>
        `;

        card.onclick = () => {
          const selected = grid.querySelectorAll(".card.selected");
          if (!card.classList.contains("selected") && selected.length >= 1) {
            selected[0].classList.remove("selected");
          }
          card.classList.toggle("selected");
          counter.textContent = grid.querySelectorAll(".card.selected").length;
        };

        grid.appendChild(card);
      }

      elections.appendChild(section);
    });

    function logout() {
      if (confirm("Are you sure you want to log out?")) {
        location.reload();
      }
    }
  </script>
